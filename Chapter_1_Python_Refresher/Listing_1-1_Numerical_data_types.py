# Arithmetic Operations

x, y = 3, 2
print("x = ", x)  # 3
print("y = ", y)  # 2
print("x + y = ", x + y)  # 5
print("x - y = ", x - y)  # 1
print("x * y = ", x * y)  # 6
print("x / y = ", x / y)  # 1.5
print("Use // to perform integer division.")
print("\tThe result is rounded down.")
print("x // y = ", x // y)  # 1
print("x % y = ", x % y)  # 1
print("-x = ", -x)  # -3
print("abs(-x) = ", abs(-x))  # 3
print("int(3.9) = ", int(3.9))  # 3
print("float(x)=", float(x))  # 3.0
print("x ** y = ", x ** y)  # 9
