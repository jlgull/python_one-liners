# Keywords: and, or, not

x, y = True, False

print("\n x = ", x)
print("\n y = ", y)
print("\n (x or y) == True:\t", (x or y) == True)  # True
print("\n (x and y) == False:\t", (x and y) == False)  # True
print("\n (not y) == True:\t", (not y) == True)  # True
